package voter.card.service;

public class Person {

	String name;
	Long adhar_card_no;
	int age;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getAdhar_card_no() {
		return adhar_card_no;
	}
	public void setAdhar_card_no(Long adhar_card_no) {
		this.adhar_card_no = adhar_card_no;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
